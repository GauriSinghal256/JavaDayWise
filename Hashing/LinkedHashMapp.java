// here in linked hashmap the order of keys are according to order of insertion 
import java.util.*;
public class LinkedHashMapp {
    public static void main(String[] args) {
        LinkedHashMap<String , Integer> lhm = new LinkedHashMap<>();
        lhm.put("India" , 100);
        lhm.put("China" , 150);
        lhm.put("US" , 50);
        lhm.put("Nepal" , 10);
        System.out.println(lhm);

        // everything is same as that of the hashmap but only one difference is there that in
        // hashmap there is no order to store keys but if we talk about linked hashmap then there is order in which we have inserted the keys will come out in that order only 





    }
}
